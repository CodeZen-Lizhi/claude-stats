        if let icon = systemSymbolMigrationMap[systemName] {
            return icon
        }
        #if DEBUG
        migrationLogger.fault("Missing FunctionalIcon migration for SF Symbol '\(systemName, privacy: .public)'")
        #endif
        return .placeholder
    }

